// primitive: string, number, boolean
const someString: string = "";

const someNumber: number = 321;

const someBoolean: boolean = false;

// other primitives: bigint, symbol

// non-primitive
const someFunction: () => number = () => 3;

const someArray: number[] = [3, 2, 1];

const someObject: { greeting: string } = {
  greeting: "Hello World!",
};

